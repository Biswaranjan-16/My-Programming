public class User {
	public String getnamebyid(Integer id) {

		if (id == 100) {
			return "Biswa";
			
		} else if (id == 101) {
			return "tiku";
			
		} else if (id == 102) {
			return "miku";
			
		} else if (id == 102) {
			return "subhu";
			
		}else {
			return null;
		}
	}
}