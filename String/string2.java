package String;

public class string2 {

	public static void main(String[] args) {
		String str="Biswaranjan";
		System.out.println("original string is :"+str);
		int val=str.codePointAt(1);
		int val2=str.codePointAt(8);
		System.out.println("unicode value of index 1"+val);
		System.out.println("unicode value of index 8"+val2);
		

	}

}
